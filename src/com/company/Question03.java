    final private static String[] treeLayout = input.split("\n");
    final private static int treeLayoutRows = treeLayout.length;
    final private static int treeLayoutColumns = treeLayout[0].length();

    private static long countTrees(int right, int down){
        int column = 0;
        long trees = 0;
        for (int row = 0; row < treeLayoutRows; row += down){
            String treeRow = treeLayout[row];
            if (treeRow.charAt(column) == '#'){
                trees++;
            }
            column = (column + right) % treeLayoutColumns;
        }

        return trees;
    }

